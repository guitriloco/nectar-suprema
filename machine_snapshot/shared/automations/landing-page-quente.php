        // Preencher UTM params do URL
        const urlParams = new URLSearchParams(window.location.search);
        const utmFields = ['utm_source', 'utm_medium', 'utm_campaign', 'utm_content', 'utm_term'];
        utmFields.forEach(field => {
            const val = urlParams.get(field);
            if (val) document.getElementById(field).value = val;
        });

        // Form submit com AJAX
        document.getElementById('leadForm').addEventListener('submit', async function(e) {
            e.preventDefault();

            const form = e.target;
            const submitBtn = document.getElementById('submitBtn');
            const successAlert = document.getElementById('alert-success');
            const errorAlert = document.getElementById('alert-error');

            // Feedback visual
            submitBtn.disabled = true;
            submitBtn.textContent = '⏳ Enviando...';
            successAlert.style.display = 'none';
            errorAlert.style.display = 'none';

            try {
                const response = await fetch(form.action, {
                    method: 'POST',
                    body: new FormData(form)
                });

                const result = await response.json();

                if (result.status === 'ok') {
                    // Sucesso
                    successAlert.style.display = 'block';
                    form.reset();
                    fbq('track', 'Lead'); // Facebook Pixel Lead event
                    gtag('event', 'generate_lead'); // GA event

                    // Mostrar modal
                    document.getElementById('successModal').style.display = 'flex';

                    // Esconder modal após 5s
                    setTimeout(() => {
                        document.getElementById('successModal').style.display = 'none';
                    }, 5000);
                } else {
                    throw new Error(result.message || 'Erro desconhecido');
                }
            } catch (err) {
                errorAlert.style.display = 'block';
                errorAlert.textContent = '❌ ' + (err.message || 'Algo deu errado. Tente novamente.');
            } finally {
                submitBtn.disabled = false;
                submitBtn.textContent = '📥 BAIXAR LISTA GRÁTIS';
            }
        });
    </script>
